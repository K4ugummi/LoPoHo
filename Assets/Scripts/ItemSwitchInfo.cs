
public class ItemSwitchInfo {

    public int fromID;
    public int fromTypeID;
    public int toID;
    public int toTypeID;

    public ItemSwitchInfo(int _fromID, int _fromTypeID, int _toID, int _toTypeID) {
        fromID = _fromID;
        fromTypeID = _fromTypeID;
        toID = _toID;
        toTypeID = _toTypeID;
    }

}
